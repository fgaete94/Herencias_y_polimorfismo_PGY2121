public abstract class User implements Like {
    
    protected String usuario, clave;
    protected int timpoConectado, like;

    public User() {
    }

    public User(String usuario, String clave, int timpoConectado) {
        this.usuario = usuario;
        this.clave = clave;
        this.timpoConectado = timpoConectado;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getClave() {
        return clave;
    }

    public void setClave(String clave) {
        this.clave = clave;
    }

    public int getTimpoConectado() {
        return timpoConectado;
    }

    public void setTimpoConectado(int timpoConectado) {
        this.timpoConectado = timpoConectado;
    }

    public int getLike() {
        return like;
    }

    public void setLike(int like) {
        this.like = like;
    }
    
    

    @Override
    public String toString() {
        return "User{" + "usuario=" + usuario + ", clave=" + clave + ", timpoConectado=" + timpoConectado + '}';
    }
    
    //listar
    
    public abstract void listar();
    
    //Like
   @Override
    public void acumulaLike(){
       int like=0;
       like=like+this.like;

        
    }
    
}
